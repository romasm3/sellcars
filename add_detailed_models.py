def add_detailed_models():
    """Add detailed model variants for major brands"""

    models_data = {
        "BMW": bmw_models,
        "Mercedes-Benz": mercedes_models,
        "Audi": audi_models,
    }

    total_added = 0

    for brand_name, model_list in models_data.items():
        brand = Brand.objects.get(name=brand_name)
        print(f"\n{brand_name}:")

        for model_name in model_list:
            car_model, created = CarModel.objects.get_or_create(
                brand=brand, name=model_name
            )
            if created:
                total_added += 1
                print(f"  + {model_name}")

    print(f"\n✅ Added {total_added} new model variants")
    print(f"   Total models in database: {CarModel.objects.count()}")


if __name__ == "__main__":
    add_detailed_models()
